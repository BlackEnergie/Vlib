<h4>La sécurité</h4>
<hr><br/>
<p>
Soyez vigilants et attentifs aux autres en respectant le code de la route et les règles de bonne conduite à vélo.
Bon à savoir, votre assurance multirisque habitation couvre également les risques liés à la responsabilité civile lors d’un parcours à vélo.

<h4>Quelques rappels</h4>
<br/>
 - Faire attention aux autres usagers de la route
 <br/>
 - Ne pas traverser sur les passages réservés aux seuls piétons
 <br/>
 - Ne pas dépasser les véhicules par la droite (risque d'angle mort)
 <br/>
 - Rouler de préférence sur les voies cyclables
 <br/>
 - Ne pas serrer les voitures en stationnement (ouverture de portières)
 <br/>
 - Rouler en file indienne lorsque vous êtes à plusieurs
 <br/>
 - Ne pas monter, ni rouler sur les trottoirs
 <br/>
 - Ne pas transporter de passager sur le vélo
 <br/>
 - Ne pas franchir les carrefours au feu rouge (respect du code de la route)
 <br/>
 - Ne pas prêter votre vélo ainsi que votre carte Vélib'
 <br/>
 - Ne pas emprunter les sens interdits (sauf contresens cyclables)
 <br/>
 - Pour un arrêt même de courte durée, attachez le vélo à un arceau de stationnement avec l'anti-vol fourni
 <br/>
 - Signaler avec le bras vos changements de direction
 <br/>
 - Avant de circuler, vérifier le bon état des éléments de sécurité : éclairage, freins, pneus
 <br/>
 - Augmentez votre visibilité la nuit (port de vêtements clairs et d'accessoires réfléchissants)
 <br/>
 - Rouler à droite sur la chaussée (sauf aménagement spécifique)
 <br/>
 - Port du casque recommandé
 <br/>
 - Respecter la règle de la priorité à droite
 <br/>


<br/>
Attention aux angles morts !
<br/>


Sécurité angles morts

Ils sont la principale cause d’accident à vélo alors prudence ! L’angle mort désigne la partie de la route invisible pour le conducteur d’un véhicule.En voiture, les parties masquées de la chaussée sont les côtés arrières droit et gauche. Pour les camions, il faut ajouter l’avant du véhicule : du fait de sa hauteur, le camion dispose d’une visibilité limitée, voire nulle.

- Vérifiez que vous pouvez toujours voir les yeux du conducteur dans les rétroviseurs, le signe que lui aussi peut vous voir.
<br/>
- Ne doublez jamais un véhicule par la droite.
<br/>
- Gardez vos distances lorsqu’un camionneur manœuvre.
<br/>
- A l’arrêt, évitez de vous immobiliser sous les rétroviseurs d’un camion ou pire devant lui.<br/></p>


