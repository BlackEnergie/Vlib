<h2>Les Stations</h2>
<hr>
<br/>
<p>
<h4>La borne</h4>

La borne Vélib’ vous permet de :
- Louer un vélo
- Consulter le mode d’emploi et des informations sur le service
- Consulter un plan des stations voisines au dos de la borne
- Se renseigner sur la disponibilité des places et des vélos en temps réel dans les stations voisines
- Vérifier que vous n'avez plus de vélo en cours de location
- Vérifier votre temps de location à la fin de votre trajet : éditez un reçu depuis la borne après avoir rendu votre vélo.

<h4>Les points d'attache des vélos</h4>

Chaque station Vélib’ est équipée de points d'attache dont le nombre peut atteindre 70, selon l'environnement et la fréquentation de la station.

Les points d'attache sont composés d’un système de verrouillage, d’un voyant lumineux et d'un lecteur de cartes. Le témoin lumineux vous indique en fonction de sa couleur si le vélo est disponible ou non.

Lorsque vous rendez votre vélo, attendez que le double bip retentisse et que le voyant passe de l'orange au vert.</p>

<br/>
<h4>Les vélos</h4>
<hr>
<p>Vlib met à votre disposition plus de 20 000 vélos. Près de 160 hommes et femmes sont présents quotidiennement sur le terrain pour garantir qu’à chaque trajet, vous empruntez un vélo en parfait état de fonctionnement et vous assurant des conditions de sécurité optimale.
<br/>
<h4>Pour votre sécurité</h4>
<br/>
Des vélos conçus et améliorés pour votre sécurité :
 - La sonnette a été optimisée pour être entendue même les jours de pluie
 - Le guidon a été renforcé, par mesure de prévention, pour résister à une utilisation intensive des vélos
 - Un tendeur de chaîne a été installé, elle reste ainsi bien tendue entre les deux pignons du vélo
 - Des conseils de sécurité, notamment sur les angles morts des camions, ont été apposés sur le guidon des vélos
 - Les feux éclairent tout au long du trajet, et l’éclairage du feu arrière fonctionne à l'arrêt pendant 120 secondes
 - Des bandes réfléchissantes sont présentes sur les roues
 - Les freins avant et arrière sont intégrés dans les moyeux des roues

Des équipes sur le terrain :
 - Les équipes de maintenance se déplacent de station en station pour effectuer les réparations légères sur place, comme une roue voilée ou un pneu dégonflé.
 - Un feu ou un frein endommagé : les vélos sont alors systématiquement retirés de la circulation pour être réparés en atelier par les mécaniciens cycle.
